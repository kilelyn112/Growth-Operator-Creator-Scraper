// Helper function to get all niches as a flat array
export function getAllNiches(): Niche[] {
  const niches: Niche[] = [];
  for (const category of NICHE_CATEGORIES) {
    for (const subcategory of category.subcategories) {
      niches.push(...subcategory.niches);
    }
  }
  return niches;
}

// Helper function to get niche by ID
export function getNicheById(id: string): Niche | undefined {
  return getAllNiches().find((niche) => niche.id === id);
}

// Helper function to search niches by query
export function searchNiches(query: string): Niche[] {
  const lowerQuery = query.toLowerCase();
  return getAllNiches().filter(
    (niche) =>
      niche.name.toLowerCase().includes(lowerQuery) ||
      niche.searchKeywords.some((kw) => kw.toLowerCase().includes(lowerQuery))
  );
}

// Get count of niches
export function getNicheCount(): number {
  return getAllNiches().length;
}
